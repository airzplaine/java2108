package com.example.chapter02;

public class Variable2 {

	public static void main(String[] args) {
		int level = 10;
		System.out.println(level);
		/*
		 * 변수 명명 규칙
		 * 1. 영숫자, 특수문자 중 $, _만 사용 가능
		 * 2. 숫자로 시작할 수 없다. => 알파벳, 특수문자로만 시작
		 * 3. 예약어는 사용할 수 없다. 예) int, while, break 등...
		 * 4. 목적에 맞게 의미를 부여하여 만드는 것이 좋다. 예) ns(X) numberOfStudent(O)
		 *    두개 이상의 단어를 조합할 때에는 시작은 소문자로
		 *    두번째 단어는 대문자로 표기하는것이 좋다.
		 *    예) 생년 -> birthYear / 이는 암묵적인 룰.
		 */
	} // end of main()

} // end of class
