package com.example.chapter02;

public class Variable1 {

	public static void main(String[] args) {
		// 여기서부터 코딩 시작
		// 변수란: 데이터를 보관하는 기억장소(의 이름)
		// 변수 선언의 기본 구문
		// 타입 변수명;
		int level; // int - 정수형 선언 | level - 변수명
		int age;
		level = 10;  // = assignment operator
		age = 19;
		// 변수 level에 정수형 숫자 값 10을 대입하여라
		System.out.println(level);
		System.out.println(age);
		// System 클래스의 out의 println 함수를 이용하여 level을 출력하라
	}// end of main()

}// end of class Variable1
